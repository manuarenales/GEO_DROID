

using Fluxor;

namespace GEO_DROID.Store.ModuloCaptura
{
    public static class ModuloCapturaReducer
    {

        [ReducerMethod]
        public static ModuloCapturaState ChangeModuloCapturaSelectedAction(ModuloCapturaState state, ChangeModuloCapturaSelectedAction action)
        {
            return state with { ModuloCapturaSelected = action.ModuloCaptura };
        }
    }
}
